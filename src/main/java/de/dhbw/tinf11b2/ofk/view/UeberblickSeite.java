	private Chart setChartBarEinzel(String[] kategorien, Double[] werte,
			String name) {

		Chart chart = new Chart(ChartType.BAR);
		chart.setWidth("90%");
		chart.setHeight("90%");
		// Modify the default configuration a bit
		Configuration conf = chart.getConfiguration();
		conf.setTitle("In dieser Kategorie bist du:");
		conf.setSubTitle(name);
		conf.getLegend().setEnabled(true); // Disable legend
		// The data

		ListSeries series = new ListSeries(name);

		series.setData(werte); // als DOUBLE Array

		conf.addSeries(series);

		// Set the category labels on the axis correspondingly
		XAxis xaxis = new XAxis();
		xaxis.setCategories(kategorien); // als String Array
		xaxis.setTitle("Kategorien");
		conf.addxAxis(xaxis);
		// Set the Y axis title
		YAxis yaxis = new YAxis();
		yaxis.setTitle("Euro");
		yaxis.getLabels().setFormatter(
				"function() {return Math.floor(this.value) + \'€\';}");
		yaxis.getLabels().setStep(2);
		conf.addyAxis(yaxis);
		return chart;
	}

	private Chart setChartBarMulti(String[] datum, Double[] werte, String name,
			Double[] werte2, String name2) {

		Chart chart = new Chart(ChartType.BAR);
		chart.setWidth("90%");
		chart.setHeight("90%");
		// Modify the default configuration a bit
		Configuration conf = chart.getConfiguration();
		conf.setTitle("In dieser Kategorie bist du:");
		conf.setSubTitle("Gesamtübersicht Zeitraum");
		conf.getLegend().setEnabled(true); // Disable legend
		// The data

		ListSeries series = new ListSeries(name);
		ListSeries series2 = new ListSeries(name2);
		series.setData(werte); // als DOUBLE Array
		series2.setData(werte2);
		conf.addSeries(series);
		conf.addSeries(series2);
		// Set the category labels on the axis correspondingly
		XAxis xaxis = new XAxis();
		xaxis.setCategories(datum); // als String Array
		xaxis.setTitle("Kategorien");
		conf.addxAxis(xaxis);
		// Set the Y axis title
		YAxis yaxis = new YAxis();
		yaxis.setTitle("Euro");
		yaxis.getLabels().setFormatter(
				"function() {return Math.floor(this.value) + \'€\';}");
		yaxis.getLabels().setStep(2);
		conf.addyAxis(yaxis);
		return chart;
	}
